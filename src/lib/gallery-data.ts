export function getGalleryItem(id: string): GalleryItem | undefined {
  return galleryItems.find(item => item.id === id);
}

export function getRelatedItems(id: string, limit = 3): GalleryItem[] {
  const current = getGalleryItem(id);
  if (!current) return [];
  const sameCategory = galleryItems.filter(item => item.id !== id && item.category === current.category);
  const others = galleryItems.filter(item => item.id !== id && item.category !== current.category);
  return [...sameCategory, ...others].slice(0, limit);
}
